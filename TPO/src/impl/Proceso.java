package impl;

public class Proceso {
	
	int nombre;

	int tiempoEst;
	private int PID;
	
	public int getNombre() {
		return nombre;
	}

	public void setNombre(int nombre) {
		this.nombre = nombre;
	}

	public int getTiempoEst() {
		return tiempoEst;
	}

	public void setTiempoEst(int tiempoEst) {
		this.tiempoEst = tiempoEst;
	}


	public int getPID() {
		return PID;
	}

	public void setPID(int idPro) {
		this.PID = idPro;
	}
	
	
	
	
}
