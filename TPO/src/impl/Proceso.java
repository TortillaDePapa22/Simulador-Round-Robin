package impl;

public class Proceso {
	
	String nombre;

	int tiempoEst;
	private int idPro;
	
	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public int getTiempoEst() {
		return tiempoEst;
	}

	public void setTiempoEst(int tiempoEst) {
		this.tiempoEst = tiempoEst;
	}


	public int getIdPro() {
		return idPro;
	}

	public void setIdPro(int idPro) {
		this.idPro = idPro;
	}
	
	
	
	
}
